#!/usr/bin/env python3
"""
Render IR into JSONL chunks for RAG.

Inputs:
  - <bundle_dir>/<bundle_name>_ir.json  (generated by build_ir.py)

Outputs:
  - <out>/<bundle_name>_docs.jsonl

Chunk types emitted:
  - entity_header
  - entity_fields
  - entity_constraints
  - entity_relationships
  - field_fact
  - glossary (optional)
  - entity_inline (optional, only for configured roots)

Usage:
  python render_chunks.py /path/to/bundle
  python render_chunks.py /path/to/bundle --out /path/to/out
  python render_chunks.py /path/to/bundle --max-fields-per-chunk 25
  python render_chunks.py /path/to/bundle --emit-inline --inline-roots "FxSwap,FxForward" --inline-max-depth 8
  python render_chunks.py /path/to/bundle --no-field-facts --no-glossary
"""

from __future__ import annotations

import argparse
import json
import re
from pathlib import Path
from typing import Any, Dict, Iterable, List, Optional, Set, Tuple

Json = Dict[str, Any]


# ----------------------------
# Helpers
# ----------------------------

def read_json(path: Path) -> Json:
    return json.loads(path.read_text(encoding="utf-8"))


def write_jsonl(path: Path, docs: Iterable[Json]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as f:
        for d in docs:
            f.write(json.dumps(d, ensure_ascii=False) + "\n")


def safe(s: str) -> str:
    s = s.strip()
    s = re.sub(r"\s+", "_", s)
    s = re.sub(r"[^A-Za-z0-9_.:#/-]+", "_", s)
    return s


def is_ref_typeref(t: Any) -> Optional[str]:
    if isinstance(t, dict) and isinstance(t.get("ref"), str):
        return t["ref"]
    return None


def cardinality_str(card: Optional[dict]) -> str:
    if not isinstance(card, dict):
        return ""
    mi = card.get("min", 0)
    ma = card.get("max", None)
    if ma is None:
        return f"[{mi}..*]"
    return f"[{mi}..{ma}]"


def short_id(eid: str) -> str:
    # For display only: try to show suffix after last '#', else last segment.
    if "#" in eid:
        return eid.split("#")[-1]
    parts = re.split(r"[/:]+", eid)
    return parts[-1] if parts else eid


def normalize_name(entity: dict) -> str:
    n = entity.get("name")
    if isinstance(n, str) and n.strip():
        return n.strip()
    return short_id(entity.get("id", "unknown"))


def collect_provenance(entity: dict) -> List[dict]:
    prov = entity.get("provenance") or []
    return [p for p in prov if isinstance(p, dict)]


def collect_source_files(entity: dict) -> List[str]:
    files: Set[str] = set()
    ann = entity.get("annotations") or {}
    if isinstance(ann, dict):
        sf = ann.get("source_file")
        if isinstance(sf, str) and sf.strip():
            files.add(sf.strip())

    # also look in provenance.locator
    for p in collect_provenance(entity):
        loc = p.get("locator")
        if isinstance(loc, str) and loc.strip():
            # loc may contain "file :: pointer" - keep only file-ish part
            files.add(loc.split(" :: ")[0].strip())
    return sorted(files)


def entity_refs(entity: dict) -> Set[str]:
    refs: Set[str] = set()

    # extends
    for x in entity.get("extends") or []:
        rid = is_ref_typeref(x)
        if rid:
            refs.add(rid)

    # fields
    for f in entity.get("fields") or []:
        if not isinstance(f, dict):
            continue
        rid = is_ref_typeref(f.get("type"))
        if rid:
            refs.add(rid)

    # array element
    rid = is_ref_typeref(entity.get("elementType"))
    if rid:
        refs.add(rid)

    # union choice options
    ch = entity.get("choice")
    if isinstance(ch, dict):
        for opt in ch.get("options") or []:
            rid2 = is_ref_typeref(opt)
            if rid2:
                refs.add(rid2)

    return refs


def estimate_len(text: str) -> int:
    # Simple char-based estimate; good enough for chunk sizing.
    return len(text)


# ----------------------------
# Rendering
# ----------------------------

def render_entity_header(bundle: dict, entity: dict) -> str:
    bn = bundle.get("name", "")
    sid = bundle.get("source_id", "")
    ver = bundle.get("version", "")
    kind = entity.get("kind", "")
    name = normalize_name(entity)
    eid = entity.get("id", "")

    src_files = collect_source_files(entity)
    provs = collect_provenance(entity)
    prov_line = ""
    if src_files:
        prov_line = f"Source files: {', '.join(src_files)}"
    elif provs:
        locs = [p.get("locator") for p in provs if isinstance(p.get("locator"), str)]
        if locs:
            prov_line = f"Provenance: {locs[0]}"

    desc = entity.get("description")
    desc_txt = desc.strip() if isinstance(desc, str) and desc.strip() else ""

    lines = []
    lines.append(f"# Entity: {name}")
    lines.append(f"EntityId: {eid}")
    lines.append(f"Bundle: {bn} | Source: {sid} {ver} | Kind: {kind}")
    if prov_line:
        lines.append(prov_line)
    if desc_txt:
        lines.append("")
        lines.append("Description:")
        lines.append(desc_txt)
    return "\n".join(lines).strip() + "\n"


def render_field_line(field: dict) -> str:
    fname = field.get("name", "")
    ftype = field.get("type", "any")
    card = field.get("cardinality")
    req = field.get("required")
    req_txt = ""
    if isinstance(req, bool):
        req_txt = " required" if req else ""

    type_txt = ""
    rid = is_ref_typeref(ftype)
    if rid:
        type_txt = f"ref:{short_id(rid)}"
    elif isinstance(ftype, str):
        type_txt = ftype
    else:
        type_txt = "any"

    c = cardinality_str(card)
    line = f"- {fname} : {type_txt} {c}{req_txt}".rstrip()
    return line


def render_field_details(field: dict) -> List[str]:
    out: List[str] = []
    desc = field.get("description")
    if isinstance(desc, str) and desc.strip():
        out.append(f"  - description: {desc.strip()}")

    constraints = field.get("constraints") or []
    if isinstance(constraints, list) and constraints:
        # keep it short; detailed constraints appear in entity_constraints chunk
        kinds = []
        for c in constraints:
            if isinstance(c, dict) and isinstance(c.get("kind"), str):
                kinds.append(c["kind"])
        if kinds:
            out.append(f"  - constraints: {', '.join(sorted(set(kinds)))}")

    ex = field.get("examples") or []
    if isinstance(ex, list) and ex:
        # show up to 2 examples
        ex2 = ex[:2]
        out.append(f"  - examples: {json.dumps(ex2, ensure_ascii=False)}")
    return out


def render_entity_fields_chunk(bundle: dict, entity: dict, fields: List[dict], idx: int, total: int) -> str:
    name = normalize_name(entity)
    lines = []
    lines.append(f"## Fields for {name} ({idx}/{total})")
    for f in fields:
        if not isinstance(f, dict):
            continue
        lines.append(render_field_line(f))
        lines.extend(render_field_details(f))
    return "\n".join(lines).strip() + "\n"


def render_constraints_block(title: str, constraints: List[dict]) -> str:
    lines = []
    lines.append(title)
    for c in constraints:
        if not isinstance(c, dict):
            continue
        kind = c.get("kind", "constraint")
        target = c.get("target")
        pieces = [f"- kind={kind}"]
        if isinstance(target, str) and target.strip():
            pieces.append(f"target={target}")
        if "values" in c:
            v = c.get("values")
            if isinstance(v, list):
                pieces.append(f"values={v[:20]}{'...' if len(v) > 20 else ''}")
        if isinstance(c.get("pattern"), str):
            pieces.append(f"pattern={c['pattern']}")
        if c.get("minimum") is not None or c.get("maximum") is not None:
            pieces.append(f"range=[{c.get('minimum')},{c.get('maximum')}]")
        if c.get("minLength") is not None or c.get("maxLength") is not None:
            pieces.append(f"length=[{c.get('minLength')},{c.get('maxLength')}]")
        if isinstance(c.get("format"), str):
            pieces.append(f"format={c['format']}")
        if "const" in c:
            pieces.append(f"const={c.get('const')}")
        if isinstance(c.get("message"), str):
            pieces.append(f"message={c['message']}")
        lines.append("  " + " ".join(pieces))
    return "\n".join(lines).strip() + "\n"


def render_entity_constraints(bundle: dict, entity: dict) -> str:
    # collect entity + field constraints
    all_constraints: List[dict] = []
    ent_constraints = entity.get("constraints") or []
    if isinstance(ent_constraints, list):
        all_constraints.extend([c for c in ent_constraints if isinstance(c, dict)])

    for f in entity.get("fields") or []:
        if not isinstance(f, dict):
            continue
        fc = f.get("constraints") or []
        if isinstance(fc, list):
            for c in fc:
                if isinstance(c, dict):
                    # ensure target has some path
                    if "target" not in c and isinstance(f.get("name"), str):
                        c = dict(c)
                        c["target"] = f"{normalize_name(entity)}.{f['name']}"
                    all_constraints.append(c)

    if not all_constraints:
        return ""

    name = normalize_name(entity)
    return render_constraints_block(f"## Constraints for {name}", all_constraints)


def render_entity_relationships(bundle: dict, entity: dict) -> str:
    refs = sorted(entity_refs(entity))
    extends = entity.get("extends") or []
    name = normalize_name(entity)

    lines = []
    lines.append(f"## Relationships for {name}")

    if extends:
        lines.append("Extends:")
        for ex in extends:
            rid = is_ref_typeref(ex)
            if rid:
                lines.append(f"- ref:{rid}")
            else:
                lines.append(f"- {ex}")

    if refs:
        lines.append("References:")
        for r in refs:
            lines.append(f"- {r}")

    # provenance summary
    src_files = collect_source_files(entity)
    if src_files:
        lines.append("Provenance files:")
        for sf in src_files:
            lines.append(f"- {sf}")

    return "\n".join(lines).strip() + "\n"


def render_field_fact(bundle: dict, entity: dict, field: dict) -> Optional[str]:
    if not isinstance(field, dict):
        return None
    ename = normalize_name(entity)
    fname = field.get("name")
    if not isinstance(fname, str) or not fname.strip():
        return None

    path = f"{ename}.{fname}"
    ftype = field.get("type", "any")
    rid = is_ref_typeref(ftype)
    type_txt = f"ref:{short_id(rid)}" if rid else (ftype if isinstance(ftype, str) else "any")
    c = cardinality_str(field.get("cardinality"))

    lines = []
    lines.append(f"# Field: {path}")
    lines.append(f"Type: {type_txt} {c}".rstrip())

    desc = field.get("description")
    if isinstance(desc, str) and desc.strip():
        lines.append(f"Description: {desc.strip()}")

    cons = field.get("constraints") or []
    if isinstance(cons, list) and cons:
        # concise constraint summary
        kinds = []
        for cc in cons:
            if isinstance(cc, dict) and isinstance(cc.get("kind"), str):
                kinds.append(cc["kind"])
        if kinds:
            lines.append(f"Constraints: {', '.join(sorted(set(kinds)))}")

    # provenance hint
    provs = field.get("provenance") or []
    if isinstance(provs, list) and provs:
        locs = [p.get("locator") for p in provs if isinstance(p, dict) and isinstance(p.get("locator"), str)]
        if locs:
            lines.append(f"Provenance: {locs[0]}")

    return "\n".join(lines).strip() + "\n"


def render_glossary(bundle: dict, entities: List[dict], max_chars: int) -> List[str]:
    # return one or more glossary chunks
    items = []
    for e in entities:
        if not isinstance(e, dict):
            continue
        name = normalize_name(e)
        eid = e.get("id", "")
        kind = e.get("kind", "")
        desc = e.get("description", "")
        desc_short = (desc.strip()[:120] + "…") if isinstance(desc, str) and len(desc.strip()) > 120 else (desc.strip() if isinstance(desc, str) else "")
        items.append((name.lower(), name, eid, kind, desc_short))

    items.sort(key=lambda t: t[0])

    chunks: List[str] = []
    cur_lines: List[str] = []
    cur_lines.append("# Glossary")
    cur_lines.append(f"Bundle: {bundle.get('name','')}")
    cur_len = estimate_len("\n".join(cur_lines)) + 1

    for _, name, eid, kind, desc_short in items:
        line = f"- {name} ({kind}) id={eid}"
        if desc_short:
            line += f" — {desc_short}"
        add_len = estimate_len(line) + 1
        if cur_len + add_len > max_chars and len(cur_lines) > 3:
            chunks.append("\n".join(cur_lines).strip() + "\n")
            cur_lines = ["# Glossary (cont.)", f"Bundle: {bundle.get('name','')}"]
            cur_len = estimate_len("\n".join(cur_lines)) + 1
        cur_lines.append(line)
        cur_len += add_len

    if cur_lines:
        chunks.append("\n".join(cur_lines).strip() + "\n")

    return chunks


