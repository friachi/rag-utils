def render_inline_tree(inline_obj: Any, indent: int = 0, max_lines: int = 2000) -> List[str]:
    """
    Render entity.annotations.inline structure into a compact, readable outline.
    This is deterministic and safe for large structures (bounded by max_lines).
    """
    lines: List[str] = []
    prefix = "  " * indent

    if len(lines) >= max_lines:
        return lines

    if isinstance(inline_obj, dict) and "ref" in inline_obj and isinstance(inline_obj["ref"], str):
        ann = inline_obj.get("annotations") or {}
        if isinstance(ann, dict) and ann.get("cycle") is True:
            lines.append(prefix + f"- ref:{short_id(inline_obj['ref'])} (cycle)")
        elif isinstance(ann, dict) and ann.get("depth_cutoff") is True:
            lines.append(prefix + f"- ref:{short_id(inline_obj['ref'])} (depth_cutoff)")
        else:
            lines.append(prefix + f"- ref:{short_id(inline_obj['ref'])}")
        return lines

    if not isinstance(inline_obj, dict):
        lines.append(prefix + f"- {str(inline_obj)}")
        return lines

    kind = inline_obj.get("kind", "")
    name = inline_obj.get("name") or short_id(inline_obj.get("id", ""))
    lines.append(prefix + f"- {name} ({kind})")

    # fields
    fields = inline_obj.get("fields") or []
    if isinstance(fields, list) and fields:
        for f in fields:
            if not isinstance(f, dict):
                continue
            fname = f.get("name", "")
            ftype = f.get("type")
            card = f.get("cardinality")
            c = cardinality_str(card)
            if isinstance(ftype, dict) and "ref" in ftype:
                lines.append(prefix + f"  * {fname}: ref:{short_id(ftype['ref'])} {c}".rstrip())
            elif isinstance(ftype, str):
                lines.append(prefix + f"  * {fname}: {ftype} {c}".rstrip())
            else:
                lines.append(prefix + f"  * {fname}: any {c}".rstrip())

            # recurse on nested inline types only if it's a dict without "ref"
            if isinstance(ftype, dict) and "ref" not in ftype:
                if len(lines) >= max_lines:
                    break
                lines.extend(render_inline_tree(ftype, indent=indent + 2, max_lines=max_lines - len(lines)))

            if len(lines) >= max_lines:
                break

    # elementType for arrays
    if kind == "array":
        et = inline_obj.get("elementType")
        if isinstance(et, dict) and "ref" in et:
            lines.append(prefix + f"  * items: ref:{short_id(et['ref'])}")
        elif isinstance(et, str):
            lines.append(prefix + f"  * items: {et}")
        elif isinstance(et, dict):
            lines.append(prefix + "  * items:")
            lines.extend(render_inline_tree(et, indent=indent + 2, max_lines=max_lines - len(lines)))

    return lines[:max_lines]


