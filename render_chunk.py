# Main: build docs
# ----------------------------

def build_docs(
    ir: Json,
    max_chars_per_chunk: int,
    max_fields_per_chunk: int,
    emit_field_facts: bool,
    emit_glossary_docs: bool,
    emit_inline: bool,
    inline_roots: Optional[List[str]],
    inline_max_lines: int,
) -> List[Json]:
    bundle = ir.get("source_bundle") or {}
    if not isinstance(bundle, dict):
        bundle = {}
    entities = ir.get("entities") or []
    if not isinstance(entities, list):
        entities = []

    bn = bundle.get("name", "bundle")
    sid = bundle.get("source_id", "")
    ver = bundle.get("version", "")
    out_docs: List[Json] = []

    # Index by id for inline root selection and metadata
    ent_by_id: Dict[str, dict] = {}
    for e in entities:
        if isinstance(e, dict) and isinstance(e.get("id"), str):
            ent_by_id[e["id"]] = e

    # Root selection for inline:
    # - If inline_roots provided, treat them as:
    #   - exact entity ids if match
    #   - else match against entity.name or short_id suffix case-insensitively
    root_ids: Set[str] = set()
    if emit_inline:
        if inline_roots:
            wanted = [r.strip() for r in inline_roots if r and r.strip()]
            wl = [w.lower() for w in wanted]
            for eid, e in ent_by_id.items():
                nm = normalize_name(e).lower()
                sid_short = short_id(eid).lower()
                if eid in wanted or nm in wl or sid_short in wl:
                    root_ids.add(eid)
        else:
            # no roots specified => inline any entity that already has annotations.inline
            for eid, e in ent_by_id.items():
                ann = e.get("annotations") or {}
                if isinstance(ann, dict) and "inline" in ann:
                    root_ids.add(eid)

    # Render entity docs
    for eid in sorted(ent_by_id.keys()):
        e = ent_by_id[eid]
        name = normalize_name(e)
        kind = e.get("kind", "")

        refs = sorted(entity_refs(e))
        src_files = collect_source_files(e)

        base_meta = {
            "bundle": bn,
            "source_id": sid,
            "version": ver,
            "entity_id": eid,
            "entity_name": name,
            "kind": kind,
            "refs": refs,
            "source_files": src_files,
        }

        # entity_header
        header_text = render_entity_header(bundle, e)
        out_docs.append({
            "doc_id": f"{bn}::entity_header::{safe(eid)}",
            "type": "entity_header",
            "metadata": base_meta,
            "text": header_text,
        })

        # entity_fields chunks
        fields = e.get("fields") or []
        if isinstance(fields, list) and fields:
            # chunk by max_fields_per_chunk and max_chars_per_chunk
            chunk: List[dict] = []
            chunk_text_preview = ""
            chunks: List[List[dict]] = []

            for f in fields:
                if not isinstance(f, dict):
                    continue
                chunk.append(f)
                # render a small preview to estimate size growth
                chunk_text_preview += render_field_line(f) + "\n"
                if len(chunk) >= max_fields_per_chunk or estimate_len(chunk_text_preview) >= max_chars_per_chunk:
                    chunks.append(chunk)
                    chunk = []
                    chunk_text_preview = ""

            if chunk:
                chunks.append(chunk)

            total = len(chunks)
            for i, ch in enumerate(chunks, start=1):
                txt = render_entity_fields_chunk(bundle, e, ch, i, total)
                out_docs.append({
                    "doc_id": f"{bn}::entity_fields::{safe(eid)}::{i:03d}",
                    "type": "entity_fields",
                    "metadata": {**base_meta, "chunk_index": i, "chunk_total": total, "paths": [f"{name}.{ff.get('name','')}" for ff in ch if isinstance(ff, dict)]},
                    "text": txt,
                })

        # entity_constraints
        cons_txt = render_entity_constraints(bundle, e)
        if cons_txt.strip():
            out_docs.append({
                "doc_id": f"{bn}::entity_constraints::{safe(eid)}",
                "type": "entity_constraints",
                "metadata": base_meta,
                "text": cons_txt,
            })

        # entity_relationships
        rel_txt = render_entity_relationships(bundle, e)
        if rel_txt.strip():
            out_docs.append({
                "doc_id": f"{bn}::entity_relationships::{safe(eid)}",
                "type": "entity_relationships",
                "metadata": base_meta,
                "text": rel_txt,
            })

        # field facts
        if emit_field_facts and isinstance(fields, list):
            for f in fields:
                fact = render_field_fact(bundle, e, f)
                if not fact:
                    continue
                fname = f.get("name", "")
                out_docs.append({
                    "doc_id": f"{bn}::field_fact::{safe(eid)}::{safe(str(fname))}",
                    "type": "field_fact",
                    "metadata": {**base_meta, "field": fname, "path": f"{name}.{fname}"},
                    "text": fact,
                })

        # inline rendering (only for selected roots)
        if emit_inline and eid in root_ids:
            ann = e.get("annotations") or {}
            inline_obj = ann.get("inline") if isinstance(ann, dict) else None
            if inline_obj is not None:
                lines = []
                lines.append(f"# Inlined schema: {name}")
                lines.append(f"EntityId: {eid}")
                lines.append(f"Bundle: {bn} | Source: {sid} {ver} | Kind: {kind}")
                lines.append("")
                outline = render_inline_tree(inline_obj, indent=0, max_lines=inline_max_lines)
                lines.extend(outline)
                txt = "\n".join(lines).strip() + "\n"

                out_docs.append({
                    "doc_id": f"{bn}::entity_inline::{safe(eid)}",
                    "type": "entity_inline",
                    "metadata": {**base_meta, "inline_root": True, "inline_max_lines": inline_max_lines},
                    "text": txt,
                })

    # glossary docs
    if emit_glossary_docs:
        gloss_chunks = render_glossary(bundle, entities=[ent_by_id[eid] for eid in sorted(ent_by_id.keys())], max_chars=max_chars_per_chunk)
        for i, txt in enumerate(gloss_chunks, start=1):
            out_docs.append({
                "doc_id": f"{bn}::glossary::{i:03d}",
                "type": "glossary",
                "metadata": {"bundle": bn, "source_id": sid, "version": ver, "chunk_index": i, "chunk_total": len(gloss_chunks)},
                "text": txt,
            })

    return out_docs


# ----------------------------
# CLI
# ----------------------------

def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("bundle_dir", type=str, help="Path to bundle folder containing <name>_ir.json")
    ap.add_argument("--out", type=str, default=None, help="Output directory (default: bundle dir)")
    ap.add_argument("--ir", type=str, default=None, help="Explicit IR file path (default: <bundle>/<name>_ir.json)")

    ap.add_argument("--max-chars-per-chunk", type=int, default=8000, help="Approx max characters per chunk (default 8000)")
    ap.add_argument("--max-fields-per-chunk", type=int, default=30, help="Max fields per entity_fields chunk (default 30)")

    ap.add_argument("--no-field-facts", action="store_true", help="Disable emitting field_fact docs")
    ap.add_argument("--no-glossary", action="store_true", help="Disable emitting glossary docs")

    ap.add_argument("--emit-inline", action="store_true", help="Emit entity_inline docs (uses entity.annotations.inline from IR)")
    ap.add_argument("--inline-roots", type=str, default=None,
                    help="Comma-separated roots to inline. Matches entity id OR entity name OR short id suffix. "
                         "If omitted, emits inline only for entities that already have annotations.inline.")
    ap.add_argument("--inline-max-lines", type=int, default=1200, help="Max outline lines in inline render (default 1200)")

    args = ap.parse_args()

    bundle_dir = Path(args.bundle_dir).resolve()
    out_dir = Path(args.out).resolve() if args.out else bundle_dir

    # Determine bundle name by scanning for *_ir.json if needed
    if args.ir:
        ir_path = Path(args.ir).resolve()
    else:
        # try to find a single *_ir.json file in bundle_dir
        cands = sorted(bundle_dir.glob("*_ir.json"))
        if len(cands) == 1:
            ir_path = cands[0]
        else:
            # fall back to bundle.yaml name if present
            yml = bundle_dir / "bundle.yaml"
            if yml.exists():
                import yaml  # optional import; only needed here
                manifest = yaml.safe_load(yml.read_text(encoding="utf-8")) or {}
                name = manifest.get("name")
                if isinstance(name, str) and name.strip():
                    ir_path = bundle_dir / f"{name.strip()}_ir.json"
                else:
                    raise FileNotFoundError(f"Could not infer IR file. Provide --ir. Candidates: {cands}")
            else:
                raise FileNotFoundError(f"Could not infer IR file. Provide --ir. Candidates: {cands}")

    if not ir_path.exists():
        raise FileNotFoundError(f"IR file not found: {ir_path}")

    ir = read_json(ir_path)
    bundle = ir.get("source_bundle") or {}
    bn = bundle.get("name", safe(ir_path.stem.replace("_ir", "")))

    inline_roots = None
    if args.inline_roots:
        inline_roots = [s.strip() for s in args.inline_roots.split(",") if s.strip()]

    docs = build_docs(
        ir=ir,
        max_chars_per_chunk=max(1000, int(args.max_chars_per_chunk)),
        max_fields_per_chunk=max(1, int(args.max_fields_per_chunk)),
        emit_field_facts=(not args.no_field_facts),
        emit_glossary_docs=(not args.no_glossary),
        emit_inline=bool(args.emit_inline),
        inline_roots=inline_roots,
        inline_max_lines=max(50, int(args.inline_max_lines)),
    )

    out_path = out_dir / f"{bn}_docs.jsonl"
    write_jsonl(out_path, docs)
    print(str(out_path))
    print(f"[render_chunks] wrote {len(docs)} docs")


if __name__ == "__main__":
    main()


